public class Ob2 {
    private String a;
    private String b;

    public Ob2(String a, String b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "Ob2{" +
                "a='" + a + '\'' +
                ", b='" + b + '\'' +
                '}';
    }
}
